        <h2>...For Administrative & Teaching Staff</h2>
        <h3>How do I submit an exam paper to go online?</h3>
        <p>Once the examination period is over papers can be emailed to us at any time, in Word or pdf format. We send out email reminders out 3 times a year, after each examination diet. If you wish to be included in the mailing list please contact us.</p>
        <h3>Can you tell me what exam papers are missing from my subject?</h3>
        <p>Yes, please email us with the details of the subject you are responsible for and we can provide a list of the papers not currently held.</p>
        <h3>How can I have a paper taken down?</h3>
        <p>Please email us with the details of the paper you wish removed.</p>
        <br>
        <br>
