        <h1>Emma Gillies: Rediscovered</h1>
        <div id="gillies-banner" class="exhibition-image"></div>
        <h4 id="individual-exhibition-h" class="right-align-h">5th December 2014 - 7th March 2015<br>Exhibition Gallery<br>Main Library<br>George Square</h4>
        <p id="individual-exhibition-p" class="right-align-p">
            Curated by: Gordon Brennan (Edinburgh College of Art, Director of Undergraduate Studies)
        </p>
        <br>
        <p id="individual-exhibition-p" class="right-align-p">
            Over the last two years, significant research has been carried out into the Edinburgh College of Art Collection.
            The project has resulted in the discovery of a number of highly significant works that add further colour to
            the history of Scottish art. One of the many extraordinary finds has been a collection of ceramics stored in
            a cupboard near to the Head of Painting's office at ECA. It became apparent early on that this collection was
            linked to Sir William Gillies in some way, as pieces from the collection appear in many of Gillies' still life
            paintings - some of which will be featured in the exhibition. Furthermore, this was an active ceramics collection
            - it was used for its original purpose as well as for the inspiration for art and, most importantly, they are
            fine works on their own merit.
        </p>
        <br>
        <p id="individual-exhibition-p" class="right-align-p">
            As research continued into the collection, it became clear that the ceramics were created mainly by Emma Gillies,
            sister of Sir William Gillies. Emma sadly died in her 30s and, particularly in light of the illustrious reputation
            of her older brother, is a peripheral figure in our understanding of 20th century art in Edinburgh. She is more
            often remembered as a tragic figure, or the sister of Sir William Gillies, than a skilled artist in her own right.
            This exhibition seeks to revisit the art of Emma Gillies.
        </p>
        <br>
