            <h2 class="object-h-head">From Bones to Boards: Space, Calculation and Structure</h2>
            <br>
            <p alt="exhibition description">A chair of mathematics was established in the University in 1620 on the intervention of the great mathematician John Napier of Merchiston, inventor of logarithms. Typically practical also was his invention of the ingenious calculating device known as Napier's bones, or rods, in which calculation became a visual process. In the later seventeenth and early eighteenth centuries James and David Gregory and Colin MacLaurin established Edinburgh mathematics firmly on the basis of the most modern thinking, and kept it turned towards the objective, practical world. MacLaurin's advice to Customs Officers about the measurement of the contents of an irregular container so that they could calculate accurately the duty on barrels of wine or brandy, or the kind of practical inquiry reflected everywhere in James Ferguson's commonplace book, are typical.
            </p>
            <br>
            <p alt="exhibition description">This direction of mathematical thought was also endorsed by the continuing loyalty of Edinburgh mathematicians to Euclid and so to geometry and to mathematics in visible form, rather than to abstract algebraic calculation. Perhaps this was the basis of the success of Scots as engineers. Indeed it has been argued that even James Clerk-Maxwell's advances in physics were in part shaped by this bias towards visible forms.
            </p>
            <br>
            <p alt="exhibition description">In the 19th and 20th centuries the extension of mathematics outwards towards astronomy or inwards towards the most elementary structures of the physical world continued to throw up striking examples of visual analysis and record. Crum Brown devised what has become the standard convention for the representation of molecular structures using his wife's knitting needles. It is fitting therefore that photographs of the two monumental figures that Eduardo Paolozzi created for the Swann Building, home of molecular biology, should be included in this section. They embody both the idea of elementary structures, but also the common ground of art and science as we see it here in the visual imagination. There is more than just superficial similarity, but real symmetry between Napier's Bones at the beginning of this story and the early computer circuit.
            </p>
