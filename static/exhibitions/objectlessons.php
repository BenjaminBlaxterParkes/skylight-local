            <h2 class="object-h-head">Housing knowledge: 400 hundred years of collections</h2>
            <br>
            <p alt="exhibition description">The history of the University's buildings vividly illustrates the importance its museums and collections have had as part of its identity and as an essential tool in teaching and research. In the seventeenth century the University already had a significant collection of portraits, hung in the library, and was starting to accumulate 'curiosities', a habit that has continued. Objects like George Buchanan's skull, Adam Ferguson's coffee pot, or the address to Sophia Jex-Blake from grateful women doctors, shown here with some of the earliest treasures of the library itself, add a special human quality to the whole story.
            </p>
            <br>
            <p alt="exhibition description">The first collections were housed in an upper hall in the library. By the middle of the eighteenth century new accommodation was needed for the museum. This was provided by raising the roof of the library. By the time Robert Adam came to draw up his plan for Old College, space for at least three separate museums is identified. In the mid-nineteenth century a large part of the collections, together with part of the site, was given to form the nucleus of what is now the Royal Museum of Scotland.
            </p>
            <br>
            <p alt="exhibition description">The next great building project, the Medical Buildings, had at its heart the great Anatomy Museum, a cathedral of learning that rose through three floors. A little earlier the first museum of musical instruments was fitted out in the Reid School. In the early 1930s, as the new science campus was developing at King's Buildings, the University Calendar lists no less than seventeen museums and the new Zoology and Geology buildings built at that time both included significant museums.
            </p>
            <br>
            <p alt="exhibition description">The story continues. In the 1970s the Torrie Collection was accommodated properly in the University for the first time and St Cecilia's Hall was fitted out to receive the Russell Collection of Keyboard Instruments. Only a few years ago, part of the Natural History collections was reinstated as the Aubrey Manning Gallery. As this exhibition demonstrates, there is now more than ever a case for a new University Museum that brings all these collections together to tell the story whose outlines are only very incompletely sketched here.
            </p>
            <br>
