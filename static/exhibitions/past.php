                <!-- EXHIBITION INFO -->
                <h2 alt="past exhibition title">Sound Body, Sound Mind: A History of Healing Through the University’s Collections</h2>
                <p alt="past exhibition dates">28th July 2017- 4th November 2017</p>
                <p alt="past exhibition description">
                    Before the 20th century, healing tended to focus on physical ailments and pains while the mental health of an individual was often sidelined and forgotten in hospitals. 
                    It was not recognized that if a person was mentally unwell, they could be treated. However with the advent of psychoanalysis and psychotherapy in the early 20th century, 
                    this slowly changed and the importance of mental health began to be acknowledged. Today, wide ranges of therapies are available, and a holistic approach to healing is being 
                    given more value. By displaying collections related to the various healing practices undertaken before the 20th century in relation to the body, and the development in attitudes 
                    towards mental health and psychotherapy in the 20th century, this exhibition aims to highlight the shift in value within healing to a consideration of the mind as well as the body. 
                    This exhibition will be the first to utilize the newly renovated space in the library gallery allowing access to non-library users. Located at the front of the library, the new 
                    space serves as an introductory room before guiding the viewer into the main gallery space
                </p>

