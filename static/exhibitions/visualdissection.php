    <h1 alt="exhibition title">Visual Dissection: The Art of Anatomy</h1>
    <h4 id="individual-exhibition-h" class="right-align-h" alt="exhibition details and location">4th December 2015 - 5th March 2016<br />
        Exhibition Gallery<br>Main Library<br>George Square<br />
        </h4>
