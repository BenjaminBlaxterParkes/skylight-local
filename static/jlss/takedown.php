        <h1>Takedown Policy</h1>
        
        <div class="content-divider-inline"><p>divider</p></div><br/>

        <p>Scottish Jewish Archive Centre has made every effort to ensure that published content
            does not infringe any person's rights, or applicable UK laws.</p>

        <p>However, if you believe that content, in any of the sites hosted on this platform, may be illegal, please
            contact the Scottish Jewish Archives Centre who will review the complaint and take appropriate
            action.</p>

        <p>Scottish Jewish Archives Centre<br/>
            Garnethill Hebrew Congregation<br/>
            129 Hill Street<br/>
            Garnethill<br/>
            Glasgow<br/>
            G3 6UB<br/>
            Tel: +44 (0)141 332 4911<br/>
            Email: <a class="email" href="info@sjac.org.uk">info@sjac.org.uk</a><br/>
        <p></p>
        <p></p>
        <p>
            <strong>Making a complaint</strong></p>

        <p>In the body of the email please provide the following information:</p>
        <p>
        <ul>
            <li>Describe the infringement in as much detail as possible so that the specific content may be readily
                identified. The URL in the address bar will allow us to lookup the specific section.
                <p></p>
            </li>
            <li>Describe your relationship to the content, e.g. I am the author/creator of the material.
                <p></p>
            </li>
            <li>Describe the grounds for complaint, some examples are:
                <p></p>
                <ul>
                    <li>Unauthorised use by reason of reproduction and/or making available the material.<p></p></li>
                    <li>Breach of the moral right of [paternity/integrity/right not to have my work subjected to
                        derogatory treatment].
                        <p></p>
                    </li>
                    <li>Other complaints, e.g. defamation, breach of confidence, data protection.<p></p></li>
                </ul>
            </li>
        </ul>
        <br>
        <p></p>
        <strong>On receipt of your complaint, the Scottish Jewish Archive Centre will:</strong><br/>
        <p></p>
        <ol>
            <li>Make an initial assessment of its validity<p></p></li>
            <li>For all but spurious complaints, temporarily remove access to the item that is subject to complaint<p></p></li>
            <li>Acknowledge receipt of the complaint by email<p></p></li>
            <li>Seek to verify your identity and authority as complainant<p></p></li>
            <li>Refer the complaint to the University's Legal Advisor for comment and advice<p></p></li>
        </ol></p>
        <p></p>
        <p><strong>Potential outcomes:</strong></p>

        <p>When the Scottish Jewish Archive Centre has verified the authenticity of your complaint and has been advised
            that it is ostensibly legitimate, the file will be permanently removed from public access.<br/>
            <p></p>
            <strong>OR</strong><br/>
            <p></p>
            If the Legal Advisor confirms that it does not breach any law then the item will be reinstated.</p>
