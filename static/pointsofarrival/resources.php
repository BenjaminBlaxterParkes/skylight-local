            <p class="page-tagline">The following websites offer resources for teachers to discuss and deepen learning with the film collection&nbsp;<em>Points of Arrival</em>.</p>

            <div class="resource-cont">
                <h2 id="learning"><a href="https://www.myjewishlearning.com/" target="_blank" rel="noopener">My Jewish Learning</a></h2>
                <p>
                    This website offers short articles on all aspects of Jewish religious life and takes a broad view of Jewish history, culture and politics. 
                    The entries are written by insiders, that is by members of Jewish communities. In addition to short explanations in a widely-accessible style, 
                    there are also articles by experts, timelines, quizzes, short videos and interviews which can be useful in the classroom.&nbsp;
                </p>
                <h2 id="viruallibrary"><a href="https://www.jewishvirtuallibrary.org/" target="_blank" rel="noopener">Jewish Virtual Library</a></h2>
                <p>
                    A website offering in-depth scholarly articles about Jewish history and religion, written in accessible style. 
                    This site is complementary to <em>My Jewish Learning</em> and addressed more towards an audience of teachers adults than school students.
                </p>
                <h2 id="virtualshetl"><a href="https://sztetl.org.pl/en/" target="_blank" rel="noopener">Virtual Shetl</a></h2>
                <p>
                    Associated with Polin – Museum of the History of Polish Jews this website collects articles and images about Jewish life in Eastern Europe. 
                    Materials are written in English accessible to secondary school students, but teacher guidance is necessary to collect useful information for 
                    class discussion. The site is regularly updated and students can research the history of specific shtetls and their inhabitants.&nbsp;
                </p>
                <h2 id="synagogues"><a href="https://sztetl.org.pl/en/" target="_blank" rel="noopener">A visual and historical record of synagogues from around the world</a></h2>
                <p>
                    Developed in association with the Museum of the Diaspora in Tel Aviv, this website gives visual access to the history of Jewish religious buildings. 
                    The website allows viewers to ‘visit’ synagogues in different places of the world today and brings to life historical buildings.
                </p>
                <h2 id="holocaustexplained"><a href="https://www.theholocaustexplained.org/" target="_blank" rel="noopener">The Holocaust Explained</a></h2>
                <p>
                        This website is associated with the Wiener Holocaust Library in London and provides an accessible guide to the history of the Holocaust and commonly asked questions. 
                        The site is aimed at secondary school students, and is useful also for teachers looking for reliable information for themselves and their classes.&nbsp;
                    
                </p>
                <p>The 
                    Wiener Library also has a number of online exhibitions which may be useful for class discussion: 
                    <a href="https://wienerlibrary.co.uk/Online_Exhibitions" target="_blank" rel="noopener">https://wienerlibrary.co.uk/Online_Exhibitions</a>   
                </p>
                <h2 id="holocaustencyclopedia"><a href="https://encyclopedia.ushmm.org/" target="_blank" rel="noopener">Holocaust Encyclopedia</a></h2>
                <p>
                    This is an online resource which is produced by experts at the United States Holocaust Memorial Museum (USHMM). 
                    It is written in a style accessible to secondary school children; and it is regularly updated. 
                    The USHMM also offers classroom resources for teachers which can easily be found on their website.
                    
                </p>
                <h2 id="migration"><a href="https://www.migrationmuseum.org/resource-bank/" target="_blank" rel="noopener">Migration Museum</a></h2>
                <p>
                    The UK-based charity, Migration Museum, offers virtual exhibitions and stories of refugees from all periods of British history. 
                    The link above is to their resource bank which contains materials aimed at teachers.
                </p>
                <h2 id="artmusicculture">Exploring Jewish Art, Music, and Culture</h2>
                <p>
                    The <a href="https://www.benuricollection.org.uk/" target="_blank" rel="noopener">Ben Uri Collection</a> offers access to a wealth of Jewish art produced by 
                    British Jews from the nineteenth century onwards. The collection provides images of artworks, artist biographies, and additional information about exhibitions.
                </p>
                <p>
                    The <a href="https://www.milkenarchive.org/" target="_blank" rel="noopener">Milken Archive of Jewish Music: 1900 – Present</a> features recordings, interviews and essays 
                    documenting the history of Jewish music in America.
                </p>
                <p>
                    The online resources of the <a href="https://thejewishmuseum.org/" target="_blank" rel="noopener">Jewish Museum</a> in New York provide a window into Jewish 
                    culture and lived Jewish experience in America.
                </p>
                <h2 id="scotland">Focus on Scotland</h2>
                <p>
                    The <a href="https://www.scojec.org/resources/education.html" target="_blank" rel="noopener">Scottish Council of Jewish Communities</a> hosts a range of educational 
                    resources which can be freely accessed by schools. In addition, the exhibition&nbsp;<em><a href="https://www.scojec.org/resources/files/jewish_experience.pdf" target="_blank" rel="noopener">The Jewish Experience in Scotland</a></em> 
                    can be viewed online.
                </p>
                <p>
                    The <a href="https://www.sjac.org.uk/schools/" target="_blank" rel="noopener">Scottish Jewish Archives Centre</a> runs educational programmes for schools, 
                    and its website offers a wealth of information for teachers. The archive is open to visitors and a preview of the wealth of materials held can be accessed 
                    in the <a href="https://sjac-collection.is.ed.ac.uk/" target="_blank" rel="noopener">Scottish Jewish Archives Centre’s Digital Collection</a>.
                </p>
