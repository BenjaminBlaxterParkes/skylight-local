        <h2>University of Aberdeen</h2>

        <h3>Collections</h3>
        <p>The University holds a huge range of collections spanning archaeology and ethnography, Scottish history, coins and medals, fine art, scientific instruments, medicine and natural sciences.</p>
        <h3>Visiting</h3>
        <p>For general information on visiting the museums, including accessibility information, see
            <a href="https://www.abdn.ac.uk/museums/about/visit-us.php"  title="Visit Us" alt-text="Visit Us" target="_blank">https://www.abdn.ac.uk/museums/about/visit-us.php</a>
            </p>

        <p>There are three museums within the University campus:</p>
        <h4>The Gallery in the Sir Duncan Rice Library</h4>
        <p>The Gallery has a programme of changing exhibitions throughout the year taken from the University’s huge collection. You don’t need to book and entry is free. For information on the latest exhibitions,
            visit <a href="https://www.abdn.ac.uk/museums/exhibitions/sdrl-679.php"  title="Sir Duncan Rice Library Gallery" alt-text="Sir Duncan Rice Library Gallery" target="_blank">https://www.abdn.ac.uk/museums/exhibitions/sdrl-679.php</a>
            </p>
        <h5>Address:</h5>
        <p>Bedford Road, Aberdeen AB24 3AA</p>
        <h5>Opening hours:</h5>
        <p>Mon – Wed, Fri: 10am – 5pm; Thurs: 10am – 7pm; Sat & Sun: 11am – 4pm</p>
        <h5>Parking:</h5>
        <p>Parking on site is reserved for university staff, but there is pay and display parking in the surrounding roads.</p>

        <h4>Zoology Museum</h4>
        <p>The museum houses the only large, international exhibition of zoological specimens in the north of Scotland.
            You can see insects, birds and even whales and even tigers. You don’t need to book and entry is free. For more information, visit
            <a href="https://www.abdn.ac.uk/museums/exhibitions/zoology-museum-588.php"  title="Zoology Museum" alt-text="Zoology Museum" target="_blank">https://www.abdn.ac.uk/museums/exhibitions/zoology-museum-588.php</a></p>
        <h5>Address:</h5>
        <p>Tillydrone Ave, Aberdeen AB24 2TZ</p>
        <h5>Opening hours:</h5>
        <p>Mon – Fri: 10am – 5pm</p>
        <h5>Parking:</h5>
        <p>Parking on site is reserved for university staff, but there is pay and display parking in the surrounding roads.</p>

        <h4>King’s Museum</h4>
        <p>This seasonal museum (it’s closed through the winter break) also houses changing exhibitions
            throughout the spring and summer. Again, you don’t need to book and entry is free. For more information on
            opening months and times, and planned exhibitions, visit
            <a href="https://www.abdn.ac.uk/museums/exhibitions/kings-museum-586.php" title="King's Museum" alt-text="King's Museum" target="_blank">https://www.abdn.ac.uk/museums/exhibitions/kings-museum-586.php</a></p>
        <h5>Address:</h5>
        <p>Old Aberdeen Town House, High Street, Aberdeen AB24 3EN</p>
        <h5>Parking:</h5>
        <p>There is no parking on site, but pay and display parking in the surrounding roads.</p>
        <h5>Group and school visits:</h5>
        <p>For group or school visits to any of the museums, you can email Susan Curran, Curator of Learning and Outreach, on
            <a href="mailto:susan.curran@abdn.ac.uk" title="Mail Susan Curran" alt-text="Mail Susan Curran">susan.curran@abdn.ac.uk</a> or call 01224 274354.</p>
