        <h2>University of Edinburgh</h2>
        <h3>Collections</h3>
        <p>Edinburgh University’s extensive collections span art, musical instruments, medicine and anatomy, natural history, geology and also encompass the University’s archives as well as the Edinburgh College of Art collection.</p?
        <h3>Visiting:</h3>
        <p>For general information on visiting the University’s museums, including accessibility information, visit
            <a href="https://www.ed.ac.uk/visit/museums-galleries"  title="University of Edinburgh Museums" alt-text="University of Edinburgh Museums" target="_blank">https://www.ed.ac.uk/visit/museums-galleries</a></p>
        <p>There are six locations within the University campus to see the collections:</p>
        <h4>Anatomical Museum</h4>
        <p>This collection of anatomy, pathology, and zoology contains over 12,000 objects which tell the story of 300 years of teaching at the University. The museums have permanent displays including anatomical
            teaching models, human skeletal remains and dried and fluid preserved specimens. PLEASE NOTE: Some of the displays contain human and other remains and might be unsettling to certain visitors.</p>
        <p>The museum is only open to visitors on certain days of the year. On these days, entry is free and there is no need to book.
            For specific opening information, please visit
            <a href="https://www.ed.ac.uk/visit/museums-galleries/anatomical"  title="University of Edinburgh Anatomical Museum" alt-text="University of Edinburgh Anatomical Museum" target="_blank">https://www.ed.ac.uk/visit/museums-galleries/anatomical</a></p>
        <h5>Research visits:</h5>
        <p>Please contact the museum directly to enquire about research visits. Email Malcolm MacCallum, Anatomical Museum Curator/Researcher on
            <a href="mailto:Malcolm.maccallum@ed.ac.uk" title="Mail Malcolm MacCallum" alt-text="Mail Malcolm MacCallum">Malcolm.maccallum@ed.ac.uk</a> or call 0131 650 2989</p>
        <h5>Address:</h5>
        <p>Doorway 3, Medical School, Teviot Place, Edinburgh EH8 9AG</p>
        <p>Visit the University’s website for specific directions.</p>
        <h5>Parking:</h5>
        <p>There are two disabled parking spaces on site. Otherwise visitors can pay and display in the surrounding streets.</p>
        <h4>Centre for Research Collections</h4>
        <p>A dedicated space to use and research the University’s historic collections. The centre offers access to and use of the rare books,
            manuscripts, archives, art and historical musical instrument collections. It is based on the 6th Floor of the University Library.</p>
        <p>Opening times are based on the academic timetables due to its use in teaching and research. For specific opening information, visit
            <a href="https://www.ed.ac.uk/visit/museums-galleries/research-collections"  title="University of Edinburgh Centre for Research Collections" alt-text="University of Edinburgh Centre for Research Collections" target="_blank">
            https://www.ed.ac.uk/visit/museums-galleries/research-collections</a></p>
        <p>To use the facility, you’ll need to make an appointment. Please contact them at
            <a href="mailto:is-crc@ed.ac.uk" title="Mail CRC" alt-text="Mail CRC">is-crc@ed.ac.uk</a></p>
        <h5>Address:</h5>
        <p>Main University Library, George Square, Edinburgh EH8 9LJ</p>
        <p>Visit the University’s website for specific directions and access information.</p>
        <h5>Parking:</h5>
        <p>There are a limited number of disabled parking spaces adjacent to George Square. In addition, there is ample pay and display parking in the square and surrounding streets.</p>
        <h4>Cockburn Geology Museum</h4>
        <p>The collection includes over 130,000 objects such as minerals, rocks and fossils. Many of the objects from the collection are displayed in cabinets throughout the corridors of the Grant Institute.</p>
        <p>If you’d like to visit, or would like to arrange a research visit, please contact the Curator via email
            <a href="mailto:cockburnmuseum@ed.ac.uk" title="Mail Cockburn Museum" alt-text="Mail Cockburn Museum">cockburnmuseum@ed.ac.uk</a> or call on 0131 650 8536. The Grant Institute is open Mon – Fri, 9am – 5pm.</p>
        <h5>Address:</h5>
        <p>Grant Institute of Earth Sciences, James Hutton Road, King’s Buildings, Edinburgh EH9 3FE</p>
        <p>Visit the University’s website for specific directions and access information</p>
        <h5>Parking:</h5>
        <p>There is disabled parking and limited free parking available within the Kings Buildings campus.</p>
