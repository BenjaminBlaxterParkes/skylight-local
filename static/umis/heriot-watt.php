        <h2>Heriot Watt University</h2>
        <p>Heriot Watt’s collections and museum can be found at both their Edinburgh and Scottish Borders campuses.
        <h3>Edinburgh</h3>
        <h4>Collections</h4>
        <p>The collections housed in Edinburgh cover the history of the University, history of the local area, and the art collection which focuses on the Edinburgh School graduates of Edinburgh College of Art, while selected sculptures can be seen around the campus.</p>
        <h4>Visiting</h4>
        <p>For individual visits you don’t need to book and entry to the museum is free.</p>
