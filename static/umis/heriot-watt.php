        <h3>Scottish Borders</h3>
        <h4>Collections</h4>
        <p>Heriot Watt’s Scottish Borders campus in Galashiels is home to the university’s textile and design collections. The collections include examples of tweed and tartan – some very rare, furnishing fabrics, paisley shawls, costumes, international textiles and lace.</p>
