        <p><a href="https://www3.rgu.ac.uk/about/art-and-heritage-collections"><strong>Robert Gordon University</strong></a>&nbsp;</p>
        <p>Robert Gordon&rsquo;s collections mostly focus on art and design - including items from the Scott Sutherland School of Architecture and Built Environment and the Gray&rsquo;s School of Art - but also encompass teaching equipment, photographs and miscellaneous items relating to the university&rsquo;s history of teaching engineering and pharmacy. They also have over 100 items of needlework and embroidery from the <a href="http://www.vam.ac.uk/content/articles/n/needlework-development-scheme/">Needlework Development Scheme</a>.</p>
        <p><em>Visiting:</em></p>
        <p>There are a number of places throughout the campus where you can see changing exhibitions of art and architecture work. To visit the exhibitions you don&rsquo;t have to book and entry is free - for more information about what is currently on display and locations, visit see the <a href="https://rguartandheritage.wordpress.com/">art collection blog</a>.</p>
        <p><em>Group, school and research visits:</em></p>
        <p>Group, school and research visits can be arranged in advance. Please contact Collections Assistant George Cheyne on <a href="mailto:g.cheyne@rgu.ac.uk">g.cheyne@rgu.ac.uk</a> or 01224 263439.</p>
